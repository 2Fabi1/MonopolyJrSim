def obsluz_nieruchomosc(gracz, numer_gracza):
    """Obs≈Çuguje logikƒô nieruchomo≈õci po przeniesieniu"""
    obecne_pole = pola[gracz['pole']]
    if obecne_pole in nieruchomosci:
        indeks = nieruchomosci.index(obecne_pole)
        koszt = math.ceil((indeks+1) / 4)
        if obecne_pole[0] == 'N': koszt += 1
        
        for i, nier in enumerate(nieruchomosci_graczy):
            if obecne_pole in nier:
                if i != numer_gracza:
                    # Sprawdzamy czy w≈Ça≈õciciel ma wszystkie nieruchomo≈õci tego koloru
                    for kolor, nier_list in grupy_kolorow.items():
                        if obecne_pole in nier_list:
                            if all(n in nieruchomosci_graczy[i] for n in nier_list):
                                koszt *= 2
                                print_with_delay(f"üí∏ PODW√ìJNY CZYNSZ! Gracz {i+1} ma ca≈Çy kolor {kolor}")
                            break
                    
                    gracz['kasa'] -= koszt
                    gracze[i]['kasa'] += koszt
                    print_with_delay(f"üè† Nieruchomo≈õƒá nale≈ºy do gracza {i+1}. Czynsz: {koszt} z≈Ç")
                else:
                    print_with_delay("‚úÖ To twoja nieruchomo≈õƒá.")
                return
                
        if gracz['kasa'] >= koszt:
            gracz['kasa'] -= koszt
            nieruchomosci_graczy[numer_gracza].append(obecne_pole)
            pozostale_nieruchomosci.remove(obecne_pole)
            print_with_delay(f"üíº Kupujesz {obecne_pole} za {koszt} z≈Ç")
            
            # Sprawd≈∫ czy gracz zdoby≈Ç ca≈Çy kolor
            for kolor, nier_list in grupy_kolorow.items():
                if obecne_pole in nier_list:
                    if all(n in nieruchomosci_graczy[numer_gracza] for n in nier_list):
                        print_with_delay(f"üéâ Gratulacje! Masz ca≈Çy kolor {kolor}!")
                    break
        else:
            print_with_delay(f"‚ùå Nie staƒá ciƒô na {obecne_pole} ({koszt} z≈Ç)")
            
            
def szansa(numer_gracza):
    """Funkcja obs≈ÇugujƒÖca kartƒô szansy"""
    gracz = gracze[numer_gracza]
    opcje = [
        ("Przejd≈∫ na nastƒôpne jasnoniebieskie pole (B≈Ç1, B≈Ç2)", ['jasnoniebieskie']),
        ("Przejd≈∫ na nastƒôpne czerwone pole (C1, C2)", ['czerwone']),
        ("Przejd≈∫ na nastƒôpne r√≥≈ºowe lub ciemnoniebieskie pole (R1, R2, N1, N2)", ['r√≥≈ºowe', 'ciemnoniebieskie']),
        ("Przejd≈∫ na nastƒôpne pomara≈Ñczowe lub zielone pole (O1, O2, Z1, Z2)", ['pomara≈Ñczowe', 'zielone']),
        ("Przejd≈∫ do Skate Parku (O1)", None, 10),
        ("Przejd≈∫ na nastƒôpne brƒÖzowe lub ≈º√≥≈Çte pole (Br1, Br2, ≈ª1, ≈ª2)", ['brƒÖzowe', '≈º√≥≈Çte']),
        ("Przejd≈∫ na nastƒôpne pomara≈Ñczowe pole (O1,O2)", ['pomara≈Ñczowe']),
        ("Przejd≈∫ na nastƒôpne jasnoniebieskie lub czerwone pole (B≈Ç1, B≈Ç2, C1, C2)", ['jasnoniebieskie', 'czerwone']),
        ("Wychodzisz z wiƒôzienia za darmo", "exit_jail"),
        ("Przejd≈∫ do Ulicy Belwederskiej (N2)", None, 22),
        ("Odbierz od ka≈ºdego gracza 1$", "collect_1"),
        ("Przesu≈Ñ siƒô o 1 pole lub we≈∫ kolejnƒÖ kartƒô szansy", "move_or_card"),
        ("Przejd≈∫ na start", None, 0),
        ("Przesu≈Ñ siƒô od 1-5 p√≥l", "move_1_5"),
        ("Zap≈Çaƒá banku 2$", "pay_2"),
        ("Odbierz 2$ od banku", "get_2")
    ]
    
    wybrana = random.choice(opcje)
    print_with_delay(f"\nüé¥ Karta Szansy: {wybrana[0]}")
    
    if wybrana[1] == "exit_jail":
        gracz['exit_jail'] = True
        print_with_delay("Masz kartƒô 'Wyjd≈∫ z wiƒôzienia' do wykorzystania!")
    
    elif wybrana[1] == "collect_1":
        for i, g in enumerate(gracze):
            if i != numer_gracza and g['kasa'] >= 1:
                g['kasa'] -= 1
                gracz['kasa'] += 1
        print_with_delay("Odebrano 1$ od ka≈ºdego gracza")
    
    elif wybrana[1] == "move_or_card":
        if random.random() < 0.5:
            przenies_gracza(gracz, (gracz['pole'] + 1) % 24)
            obsluz_nieruchomosc(gracz, numer_gracza)
        else:
            print_with_delay("Losujesz kolejnƒÖ kartƒô szansy")
            szansa(numer_gracza)
    
    elif wybrana[1] == "move_1_5":
        ruch = random.randint(1, 5)
        przenies_gracza(gracz, (gracz['pole'] + ruch) % 24)
        obsluz_nieruchomosc(gracz, numer_gracza)
    
    elif wybrana[1] == "pay_2":
        gracz['kasa'] -= 2
        print_with_delay("Zap≈Çaci≈Çe≈õ bankowi 2$")
    
    elif wybrana[1] == "get_2":
        gracz['kasa'] += 2
        print_with_delay("Otrzyma≈Çe≈õ 2$ od banku")
    
    elif len(wybrana) == 3:
        przenies_gracza(gracz, wybrana[2])
        if wybrana[2] != 0:
            obsluz_nieruchomosc(gracz, numer_gracza)
        else:
            gracz['kasa'] += 2
            print_with_delay("üí∞ Przej≈õcie przez START! +2$")
    
    else:
        kolory = wybrana[1]
        nastepne_pole = znajdz_nastepne_pole(gracz, kolory)
        if nastepne_pole is not None:
            przenies_gracza(gracz, nastepne_pole)
            obsluz_nieruchomosc(gracz, numer_gracza)
        else:
            print_with_delay("Nie znaleziono odpowiedniego pola")

def ruch(numer_gracza):
    global gracze, pozostale_nieruchomosci, nieruchomosci_graczy

    if gracze[numer_gracza]['kasa'] < 0:
        return

    # Obs≈Çuga wiƒôzienia
    if gracze[numer_gracza]['injail']:
        gracze[numer_gracza]['jail_turns'] += 1
        print_with_delay(f"‚è≥ Gracz {numer_gracza + 1} jest w wiƒôzieniu (tura {gracze[numer_gracza]['jail_turns']}/2)")
        
        if gracze[numer_gracza]['exit_jail']:
            gracze[numer_gracza]['injail'] = False
            gracze[numer_gracza]['exit_jail'] = False
            gracze[numer_gracza]['jail_turns'] = 0
            print_with_delay(f"üé¥ Gracz {numer_gracza + 1} u≈ºywa karty 'Wyjd≈∫ z wiƒôzienia' i opuszcza wiƒôzienie!")
        elif gracze[numer_gracza]['jail_turns'] >= 2:
            gracze[numer_gracza]['injail'] = False
            gracze[numer_gracza]['jail_turns'] = 0
            print_with_delay(f"üîì Gracz {numer_gracza + 1} opuszcza wiƒôzienie po 2 turach")
        else:
            return

    rzut = random.randint(1, 6)
    print_with_delay(f"\nüé≤ Gracz {numer_gracza + 1} rzuci≈Ç: {rzut}")
    pole_gracza = gracze[numer_gracza]['pole']
    pole_gracza += rzut

    if pole_gracza > 23:
        pole_gracza -= 24
        gracze[numer_gracza]['kasa'] += 2
        print_with_delay("üí∞ Przej≈õcie przez START! Gracz dostaje 2 z≈Ç")

    obecne_pole = pola[pole_gracza]
    print_with_delay(f"üìç Gracz {numer_gracza + 1} stanƒÖ≈Ç na polu: {obecne_pole}")

    if obecne_pole in nieruchomosci:
        indeks = nieruchomosci.index(obecne_pole)
        koszt = math.ceil((indeks+1) / 4)
        if obecne_pole[0] == 'N':
          koszt += 1
        nieruchomosc_zajeta = False
        for i in range(len(gracze)):
            if obecne_pole in nieruchomosci_graczy[i]:
                nieruchomosc_zajeta = True
                if i != numer_gracza:
                    gracze[numer_gracza]['kasa'] -= koszt
                    gracze[i]['kasa'] += koszt
                    print_with_delay(f"üè† Nieruchomo≈õƒá nale≈ºy do gracza {i + 1}. Czynsz: {koszt} z≈Ç")
                else:
                    print_with_delay("‚úÖ To twoja nieruchomo≈õƒá.")
                break

        if not nieruchomosc_zajeta:
            if gracze[numer_gracza]['kasa'] >= koszt:
                gracze[numer_gracza]['kasa'] -= koszt
                nieruchomosci_graczy[numer_gracza].append(obecne_pole)
                pozostale_nieruchomosci.remove(obecne_pole)
                print_with_delay(f"üíº Gracz {numer_gracza + 1} kupuje nieruchomo≈õƒá {obecne_pole} za {koszt} z≈Ç")
            else:
                print_with_delay(f"‚ùå Gracz {numer_gracza + 1} nie ma wystarczajƒÖco ≈õrodk√≥w (koszt: {koszt})")
    elif obecne_pole == 'Szansa':
        szansa(numer_gracza)
    elif obecne_pole == 'Wiƒôzienie':
        if not gracze[numer_gracza]['injail']:
            gracze[numer_gracza]['injail'] = True
            gracze[numer_gracza]['jail_turns'] = 0
            gracze[numer_gracza]['pole'] = pola.index('Wiƒôzienie')
            print_with_delay("üöì Gracz trafia do wiƒôzienia! Musi odsiedzieƒá 2 tury (chyba ≈ºe ma kartƒô 'Wyjd≈∫ z wiƒôzienia')")
        else:
            print_with_delay("üîí Gracz jest ju≈º w wiƒôzieniu - pozostaje tam dalej")
    else:
        print_with_delay("üî∏ Zwyk≈Çe pole ‚Äì nic siƒô nie dzieje.")

    gracze[numer_gracza]['pole'] = pole_gracza
    print_with_delay(f"üí∞ Kasa gracza {numer_gracza + 1}: {gracze[numer_gracza]['kasa']} z≈Ç")

def sprawdz_wygrana():
    """Sprawdza, czy kt√≥ry≈õ gracz wygra≈Ç"""
    aktywni_gracze = [g for g in gracze if g['kasa'] >= 0]
    
    if len(aktywni_gracze) == 1:
        zwyciezca = gracze.index(aktywni_gracze[0])
        print_with_delay(f"\nüéâüéâüéâ Gracz {zwyciezca + 1} wygrywa grƒô! üéâüéâüéâ")
        print_with_delay("Pow√≥d: Wszyscy inni gracze zbankrutowali")
        return True
    
    for i, nier in enumerate(nieruchomosci_graczy):
        if len(nier) == len(nieruchomosci):
            print_with_delay(f"\nüèÜüèÜüèÜ Gracz {i + 1} wygrywa grƒô! üèÜüèÜüèÜ")
            print_with_delay("Pow√≥d: Posiada wszystkie nieruchomo≈õci")
            return True
    
    return False

def pokaz_stan_graczy():
    print_with_delay("\nüìä Stan kont graczy:")
    for i, gracz in enumerate(gracze):
        status = ""
        if gracz['kasa'] < 0:
            status = " (BANKRUT)"
        elif gracz['injail']:
            status = f" (Wiƒôzienie, tura {gracz['jail_turns']}/2)"
        
        print_with_delay(f"Gracz {i + 1}: {gracz['kasa']} z≈Ç", end="")
        if gracz['exit_jail']:
            print_with_delay(" [Karta Wyjd≈∫ z Wiƒôzienia]", end="")
        print_with_delay(status)
    
    print_with_delay("\nüè† Nieruchomo≈õci:")
    for i, nier in enumerate(nieruchomosci_graczy):
        print_with_delay(f"Gracz {i + 1}: {', '.join(nier) if nier else 'brak'}")
    print_with_delay()

# Funkcja g≈Ç√≥wna
def main():
    global increment
    while True:
        print_with_delay(f'\n=== Runda: {increment+1} ===')
        ruch(increment % len(gracze))
        pokaz_stan_graczy()
        
        if sprawdz_wygrana():
            break
            
        time.sleep(0.5)  # Dodatkowe op√≥≈∫nienie miƒôdzy rundami
        increment += 1

# Start gry
if __name__ == "__main__":
    main()
